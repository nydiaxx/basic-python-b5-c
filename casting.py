x = 3.5
print(x)
print(type(x))

# float to integer
int_x = int(x)
print(int_x)
print(type(int_x))

# integer to float
float_x = float(int_x)
print(float_x)
print(type(float_x))

# float to string
desimal = 59.12
print(desimal)
print(type(desimal))

string_desimal = str(desimal)
print(string_desimal)
print(type(string_desimal))

print(string_desimal*2)
print(desimal*2)